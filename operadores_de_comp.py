# Exercício: Operadores de comparação

# 1. Crie uma variável chamada idade.
# 2. Dê um valor numérico para ela.

# 3. Mostre na tela o resultado das seguintes comparações:

# idade é maior que 18
# idade é menor que 18
# idade é igual a 18
# idade é diferente de 18
# idade é maior ou igual a 18
# idade é menor ou igual a 18

# Exemplo de saída esperada:

# Maior que 18: True
# Menor que 18: False
# Igual a 18: False
# Diferente de 18: True
# Maior ou igual a 18: True
# Menor ou igual a 18: False

idade = 20

print(f"Maior que 18: {idade > 18}")
print(f"Menor que 18: {idade < 18}")
print(f"Igual a 18: {idade == 18}")
print(f"Diferente de 18: {idade != 18}")
print(f"Maior ou igual a 18: {idade >= 18}")
print(f"Menor ou igual a 18: {idade <= 18}")